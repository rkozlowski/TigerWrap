ALTER PROCEDURE [Toolkit].[AddProjectEnumMapping]
    @projectId      SMALLINT,
    @schema         NVARCHAR(128),
    @nameMatchId    TINYINT,
    @namePattern    NVARCHAR(200),
    @escChar        NCHAR(1),
    @isSetOfFlags   BIT,
    @nameColumn     NVARCHAR(128),
    @description    NVARCHAR(500) = NULL,
    @descriptionColumn NVARCHAR(128) = NULL,
    @descriptionAttributeClassName VARCHAR(100) = NULL,
    @descriptionAttributeNamespaceName VARCHAR(100) = NULL,
    @id             INT OUTPUT,
    @errorMessage   NVARCHAR(4000) OUTPUT
AS
BEGIN
    SET NOCOUNT ON;
	SET XACT_ABORT ON;

    DECLARE 
        @RC_OK INT = 0,
        @RC_DB_ERROR INT = 1,
